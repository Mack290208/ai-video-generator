"""
layouts/constants.py
--------------------
全局画面布局常量。所有 components / templates 都应该引用本文件，
不要再硬编码坐标。

设计原则
========
1. **画面分区固定**：标题区 / 主视觉区 / 信息栏 / 字幕专区 四块。
2. **字幕专区不可侵入**：任何 Manim 内容的 y 坐标不得 < SUBTITLE_TOP_Y。
   合成阶段会硬烧 SRT 字幕，硬烧位置在画面底部约 45px 上方，
   与 SUBTITLE_TOP_Y 对应。
3. **统一字体**：中文用 Microsoft YaHei（Pango 渲染），公式走 LaTeX。

16:9 画面默认坐标范围（Manim 默认）
    x ∈ [-7.11, +7.11]   宽 14.22
    y ∈ [-4.0,  +4.0 ]   高 8.0
"""

from __future__ import annotations

# ============================================================
# 字体
# ============================================================
FONT_ZH = "Microsoft YaHei"          # 中文字体（Pango）
# 公式直接用 manim.MathTex，不在这里指定字体


# ============================================================
# 字号（统一标准，避免每个模板自定义）
# ============================================================
FS_TITLE = 34          # 主标题
FS_SUBTITLE = 28       # 副标题
FS_FORMULA_BIG = 32    # 重点公式（首次出现）
FS_FORMULA = 26        # 普通公式（信息栏）
FS_PARAM = 28          # 参数标签（α=0.25 这类）
FS_VALUE = 30          # 数值显示（θ₀ = -2.50）
FS_BODY = 22           # 一般正文 / 收敛提示
FS_AXIS = 22           # 坐标轴数字


# ============================================================
# Y 轴分区（自上而下）
# ============================================================
TITLE_Y = 3.4              # 标题中心 y
TITLE_BOTTOM_Y = 3.0       # 标题区下边界

MAIN_TOP_Y = 2.8           # 主视觉区上边界
MAIN_BOTTOM_Y = -2.4       # 主视觉区下边界（坐标系/图表都放这里）

SUBTITLE_TOP_Y = -2.8      # 字幕专区上边界，⚠️ 任何内容 y 不得低于这条线
# y < -2.8 的区域全部留给硬烧字幕


# ============================================================
# X 轴分区
# ============================================================
# 主视觉区（左侧）：坐标系 / 曲线 / 主图 居中略偏左
MAIN_CENTER_X = -1.9       # 主视觉中心 x（让出右边给信息栏）
MAIN_LEFT_X = -6.5
MAIN_RIGHT_X = +2.5

# 信息栏（右侧）：公式 / 参数 / 当前值 / 收敛提示
INFO_PANEL_X = 4.6         # 信息栏内容居中 x
INFO_PANEL_LEFT_X = 3.5    # 信息栏左边界


# ============================================================
# 信息栏 Y 站位（自上而下，给常见元素预留固定槽位）
# ============================================================
INFO_FORMULA_Y = 2.3       # 主公式（如 L(θ)=(θ-2)²）
INFO_RULE_Y = 0.8          # 更新规则 / 推导
INFO_PARAM_Y = 0.0         # 参数（α=0.25）
INFO_VALUE_Y = -0.9        # 动态刷新值（θₜ = ...）
INFO_RESULT_Y = -1.9       # 收敛 / 总结提示


# ============================================================
# 坐标系默认尺寸
# ============================================================
AXES_X_LENGTH = 7.8
AXES_Y_LENGTH = 4.2


# ============================================================
# 颜色语义（避免 import manim 循环；这些是字符串名，使用方再 import）
# ============================================================
# 在 components / templates 里：
#   from manim import BLUE, YELLOW, GREEN, WHITE
# 直接用即可。语义约定：
#
#   WHITE   - 标题、轴线、普通文本
#   BLUE    - 主曲线 / 主公式
#   YELLOW  - 当前点 / 当前参数 / 学习率（强调用）
#   GREEN   - 历史轨迹 / 最终收敛结果
#   RED     - 错误 / 警告 / 反例
#   ORANGE  - 中间步骤 / 提示
#
# （这只是约定，模板可以覆盖；但同一个视频内尽量一致。）


# ============================================================
# 安全检查工具
# ============================================================
def assert_above_subtitle(y: float, what: str = "element") -> None:
    """确认元素 y 坐标在字幕专区之上，否则会遮挡字幕。"""
    if y < SUBTITLE_TOP_Y:
        raise ValueError(
            f"[layout] {what} y={y:.2f} 低于字幕专区上界 "
            f"SUBTITLE_TOP_Y={SUBTITLE_TOP_Y}，会遮挡硬烧字幕"
        )
